

public class RatingUserMap {

	int sim;
	String user;
	public RatingUserMap(int sim, String user) {
		
		this.sim = sim;
		this.user = user;
	}
	public int getSim() {
		return sim;
	}
	public String getUser() {
		return user;
	}
	public void setSim(int sim) {
		this.sim = sim;
	}
	public void setUser(String user) {
		this.user = user;
	}
	
	
	
	
}
